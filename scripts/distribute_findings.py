def _distribute_report(findings: list[dict], report_path: Path) -> None:
    """Generate a distribution manifest."""
    dist_dir = Path("output/distributions")
    dist_dir.mkdir(parents=True, exist_ok=True)
    manifest = {
        "source_report": str(report_path),
        "distributed_at": datetime.now(timezone.utc).isoformat(),
        "total_findings": len(findings),
        "by_severity": {},
        "findings": findings,
    }
    for f in findings:
        sev = f.get("severity", "unknown")
        manifest["by_severity"][sev] = manifest["by_severity"].get(sev, 0) + 1

    out = dist_dir / f"distribution-{datetime.now().strftime('%Y%m%d-%H%M%S')}.json"
    out.write_text(json.dumps(manifest, indent=2, ensure_ascii=False), encoding="utf-8")
    print(f"    [REPORT] Distribution manifest: {out}")


